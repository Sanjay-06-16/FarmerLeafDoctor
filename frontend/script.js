// Global Application State
let currentLang = localStorage.getItem("farmer_doctor_lang") || "en";
let currentPrediction = null;
let selectedFile = null;

// DOM Elements
const langToggleBtn = document.getElementById("langToggle");
const btnCamera = document.getElementById("btnCamera");
const btnGallery = document.getElementById("btnGallery");
const cameraInput = document.getElementById("cameraInput");
const galleryInput = document.getElementById("galleryInput");
const dropZone = document.getElementById("dropZone");
const previewContainer = document.getElementById("previewContainer");
const imagePreview = document.getElementById("imagePreview");
const btnRemoveImage = document.getElementById("btnRemoveImage");
const btnReset = document.getElementById("btnReset");
const btnErrorRetry = document.getElementById("btnErrorRetry");

// Initialize application
document.addEventListener("DOMContentLoaded", () => {
  setLanguage(currentLang);
  setupEventListeners();
});

// Setup event listeners for clicks, drag and drop, inputs
function setupEventListeners() {
  // Language toggle
  langToggleBtn.addEventListener("click", () => {
    const nextLang = currentLang === "en" ? "ta" : "en";
    setLanguage(nextLang);
  });

  // Action Buttons
  btnCamera.addEventListener("click", () => cameraInput.click());
  btnGallery.addEventListener("click", () => galleryInput.click());

  // Input Changes
  cameraInput.addEventListener("change", handleFileSelection);
  galleryInput.addEventListener("change", handleFileSelection);

  // Drag and drop events on dropzone
  ["dragenter", "dragover"].forEach(eventName => {
    dropZone.addEventListener(eventName, (e) => {
      e.preventDefault();
      dropZone.classList.add("dragover");
    }, false);
  });

  ["dragleave", "drop"].forEach(eventName => {
    dropZone.addEventListener(eventName, (e) => {
      e.preventDefault();
      dropZone.classList.remove("dragover");
    }, false);
  });

  dropZone.addEventListener("drop", (e) => {
    const dt = e.dataTransfer;
    const files = dt.files;
    if (files.length > 0) {
      processFile(files[0]);
    }
  });

  // Remove preview image
  btnRemoveImage.addEventListener("click", resetUploadState);

  // Reset results and try again
  btnReset.addEventListener("click", resetUploadState);
  btnErrorRetry.addEventListener("click", resetUploadState);
}

// Sets the current UI language (re-renders static text nodes and active prediction details)
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("farmer_doctor_lang", lang);
  
  // Toggle layout class for localized font styling (optional)
  document.documentElement.lang = lang;

  // Translate all matching elements
  const translations = TRANSLATIONS[lang];
  Object.keys(translations).forEach(id => {
    const element = document.getElementById(id);
    if (element) {
      if (id === "txtDisclaimer") {
        // Disclaimer contains HTML bold tags
        element.innerHTML = translations[id];
      } else {
        // Default text update
        // Check if button text matches span selector inside btn
        const childSpan = element.querySelector("span:not(.btn-icon)");
        if (childSpan) {
          childSpan.textContent = translations[id];
        } else {
          element.textContent = translations[id];
        }
      }
    }
  });

  // Adjust toggle button style
  const innerSpan = langToggleBtn.querySelector(".lang-text");
  if (innerSpan) {
    innerSpan.textContent = translations.langText;
  }

  // Update dynamically returned prediction card if active
  if (currentPrediction) {
    updateResultDisplay();
  }
}

// Route file inputs
function handleFileSelection(e) {
  const files = e.target.files;
  if (files.length > 0) {
    processFile(files[0]);
  }
}

// Display preview of leaf image and send it to FastAPI backend
function processFile(file) {
  // Validate type
  if (!file.type.startsWith("image/")) {
    alert(currentLang === "en" 
      ? "Please select a valid image file." 
      : "தயவுசெய்து சரியான படக் கோப்பைத் தேர்ந்தெடுக்கவும்."
    );
    return;
  }

  selectedFile = file;

  // Show Image Preview
  const reader = new FileReader();
  reader.onload = (e) => {
    imagePreview.src = e.target.result;
    previewContainer.style.display = "block";
    
    // Automatically upload and analyze
    uploadImage(file);
  };
  reader.readAsDataURL(file);
}

// Send image file via POST to FastAPI predict endpoint
function uploadImage(file) {
  showSection("loadingSection");
  
  const formData = new FormData();
  formData.append("file", file);

  // Send request to FastAPI backend (handles absolute path fallback correctly)
  fetch("/predict", {
    method: "POST",
    body: formData
  })
  .then(response => {
    if (!response.ok) {
      throw new Error("Diagnosis failed. Bad file formatting or server error.");
    }
    return response.json();
  })
  .then(data => {
    if (data.success) {
      // Validate model confidence (minimum 60% required)
      const confidence = data.confidence * 100;
      if (confidence < 60) {
        showSection("errorSection");
      } else {
        currentPrediction = data;
        updateResultDisplay();
        showSection("resultsSection");
      }
    } else {
      showSection("errorSection");
    }
  })
  .catch(err => {
    console.error(err);
    showSection("errorSection");
  });
}

// Update the result card components using bilingual translations
function updateResultDisplay() {
  if (!currentPrediction) return;

  const isTa = currentLang === "ta";
  const resultCard = document.getElementById("resultCard");
  const resultTitle = document.getElementById("resultTitle");
  const confidenceBadge = document.getElementById("confidenceBadge");
  const iconHealthy = document.getElementById("iconHealthy");
  const iconDiseased = document.getElementById("iconDiseased");

  const crop = isTa ? currentPrediction.crop_ta : currentPrediction.crop;
  const disease = isTa ? currentPrediction.disease_ta : currentPrediction.disease;
  const isHealthy = currentPrediction.class_name.toLowerCase().includes("healthy");

  // Style card based on disease state
  if (isHealthy) {
    resultCard.className = "result-card healthy";
    iconHealthy.style.display = "block";
    iconDiseased.style.display = "none";
  } else {
    resultCard.className = "result-card diseased";
    iconHealthy.style.display = "none";
    iconDiseased.style.display = "block";
  }

  // Set Title & Confidence
  resultTitle.textContent = `${crop} - ${disease}`;
  confidenceBadge.textContent = `${Math.round(currentPrediction.confidence * 100)}% Match`;

  // Set fields
  document.getElementById("valCause").textContent = isTa ? currentPrediction.cause_ta : currentPrediction.cause;
  document.getElementById("valSymptoms").textContent = isTa ? currentPrediction.symptoms_ta : currentPrediction.symptoms;

  // Render remedy bullets
  const remedyList = document.getElementById("valRemedy");
  remedyList.innerHTML = "";
  const remedies = isTa ? currentPrediction.remedy_ta : currentPrediction.remedy;
  
  remedies.forEach(step => {
    const li = document.createElement("li");
    li.textContent = step;
    remedyList.appendChild(li);
  });
}

// Show/Hide page elements smoothly
function showSection(sectionId) {
  const sections = ["guideSection", "loadingSection", "errorSection", "resultsSection"];
  sections.forEach(id => {
    const el = document.getElementById(id);
    if (el) {
      el.style.display = (id === sectionId) ? "block" : "none";
    }
  });
}

// Reset upload variables and return to main state
function resetUploadState() {
  selectedFile = null;
  currentPrediction = null;
  
  // Clear inputs
  cameraInput.value = "";
  galleryInput.value = "";
  imagePreview.src = "#";
  previewContainer.style.display = "none";
  
  // Restore guide screen
  showSection("guideSection");
}
